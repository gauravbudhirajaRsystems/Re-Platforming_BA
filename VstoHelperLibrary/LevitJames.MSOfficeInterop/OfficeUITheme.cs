 
namespace LevitJames.MSOffice
{
    /// <summary>
    ///     An enum that represents the Office 2007 color themes.
    /// </summary>
    public enum OfficeUITheme
    {
        /// <summary>
        ///     No Office theme was found. This can happen if you are using an earlier version of Office than 2007
        /// </summary>
        None,

        /// <summary>
        ///     The blue theme is being used.
        /// </summary>
        Blue = 1,

        /// <summary>
        ///     The silver theme is being used.
        /// </summary>
        Silver = 2,

        /// <summary>
        ///     The black theme is being used.
        /// </summary>
        Black = 3,


        /// <summary>
        ///     Represents the 2013 White Theme
        /// </summary>
        
        White = 4,

        /// <summary>
        ///     Represents the 2013 Light Grey Theme
        /// </summary>
        
        LightGrey = 5,

        /// <summary>
        ///     Represents the 2013 Dark Grey Theme
        /// </summary>
        
        DarkGrey = 6,

        /// <summary>
        ///     Represents the 2016 Dark Grey Theme
        /// </summary>
        Colorful = 7,


        /// <summary>
        ///   Windows 10, matches the Dark Color  Represents the 2016 Dark Grey Theme
        /// </summary>
        UseSystem = 8
    }

